esp_err_t oled_init(void)
{
    /* SSD1306 initialisation sequence for 128x64 */
    static const uint8_t init_cmds[] = {
        0xAE,       /* Display OFF */
        0xD5, 0x80, /* Set display clock div ratio */
        0xA8, 0x3F, /* Set multiplex ratio (64-1) */
        0xD3, 0x00, /* Set display offset = 0 */
        0x40,       /* Set start line = 0 */
        0x8D, 0x14, /* Enable charge pump */
        0x20, 0x00, /* Memory addressing mode = horizontal */
        0xA1,       /* Segment re-map (col 127 = SEG0) */
        0xC8,       /* COM output scan direction (remapped) */
        0xDA, 0x12, /* COM pins hardware config */
        0x81, 0xCF, /* Contrast */
        0xD9, 0xF1, /* Pre-charge period */
        0xDB, 0x40, /* VCOMH deselect level */
        0xA4,       /* Entire display ON (follow RAM) */
        0xA6,       /* Normal display (not inverted) */
        0xAF,       /* Display ON */
    };

    for (size_t i = 0; i < sizeof(init_cmds); i++) {
        esp_err_t err = oled_send_cmd(init_cmds[i]);
        if (err != ESP_OK) {
            ESP_LOGE(TAG, "OLED init cmd 0x%02X failed", init_cmds[i]);
            return err;
        }
    }

    oled_clear();
    ESP_LOGI(TAG, "SSD1306 128x64 OLED initialised (addr=0x%02X)", OLED_I2C_ADDR);
    return ESP_OK;
}

void oled_clear(void)
{
    memset(fb, 0, FB_SIZE);
    oled_flush();
}

void oled_show_status(float temp, float gravity_est,
                      int relay1, int relay2, bool wifi_connected)
{
    memset(fb, 0, FB_SIZE);

    char line[17]; /* 128/8 = 16 chars max + NUL */

    /* Line 0: Title */
    fb_puts(0, 0, "FermentaBot v2");

    /* Line 1: blank separator */

    /* Line 2: Temperature */
    snprintf(line, sizeof(line), "Tmp: %5.1fC", temp);
    fb_puts(0, 2, line);

    /* Line 3: Estimated gravity (SG) */
    snprintf(line, sizeof(line), "SG:  %5.3f", gravity_est);
    fb_puts(0, 3, line);

    /* Line 4: Relay states */
    snprintf(line, sizeof(line), "Heat:%d Cool:%d",
             relay1 ? 1 : 0, relay2 ? 1 : 0);
    fb_puts(0, 4, line);

    /* Line 6: Wi-Fi status */
    fb_puts(0, 6, wifi_connected ? "WiFi: OK" : "WiFi: --");

    oled_flush();
}
