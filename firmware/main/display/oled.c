/* ------------------------------------------------------------------ */
/*  SSD1306 I2C helpers                                               */
/* ------------------------------------------------------------------ */

static esp_err_t oled_send_cmd(uint8_t cmd)
{
    uint8_t buf[2] = { 0x00, cmd };   /* Co=0, D/C#=0 (command) */
    return i2c_master_write_to_device(BME280_I2C_PORT, OLED_I2C_ADDR,
                                      buf, sizeof(buf),
                                      pdMS_TO_TICKS(100));
}

static esp_err_t oled_send_data(const uint8_t *data, size_t len)
{
    /* Build buffer: control byte (0x40 = Co=0, D/C#=1) + data */
    uint8_t *buf = malloc(len + 1);
    if (!buf) return ESP_ERR_NO_MEM;
    buf[0] = 0x40;
    memcpy(buf + 1, data, len);
    esp_err_t err = i2c_master_write_to_device(BME280_I2C_PORT, OLED_I2C_ADDR,
                                                buf, len + 1,
                                                pdMS_TO_TICKS(200));
    free(buf);
    return err;
}

/** Push the entire framebuffer to the display. */
static void oled_flush(void)
{
    /* Set column address 0..127 */
    oled_send_cmd(0x21); oled_send_cmd(0x00); oled_send_cmd(0x7F);
    /* Set page address 0..7 */
    oled_send_cmd(0x22); oled_send_cmd(0x00); oled_send_cmd(0x07);

    /* Send in 128-byte (one page) chunks to stay within I2C limits */
    for (int page = 0; page < 8; page++) {
        oled_send_data(&fb[page * OLED_WIDTH], OLED_WIDTH);
    }
}

/* ------------------------------------------------------------------ */
/*  Text rendering into the framebuffer                               */
/* ------------------------------------------------------------------ */

/**
 * Draw a character at pixel position (x, page).
 * page is 0..7 (each page = 8 vertical pixels).
 */
static void fb_putchar(int x, int page, char c)
{
    if (c < 0x20 || c > 0x7E) c = '?';
    int idx = c - 0x20;
    int offset = page * OLED_WIDTH + x;
    if (offset + 8 > FB_SIZE) return;
    memcpy(&fb[offset], font8x8[idx], 8);
}

/**
 * Draw a string at (x, page).  Returns the x position after the last
 * character.
 */
static int fb_puts(int x, int page, const char *str)
{
    while (*str && x + 8 <= OLED_WIDTH) {
        fb_putchar(x, page, *str++);
        x += 8;
    }
    return x;
}

